const apiKey = "3706929e29f1cb90462c004c57e39cb7";
const apiUrl = "https://api.openweathermap.org/data/2.5/weather?units=metric&q=";

const searchBox = document.querySelector(".pesquisa input");
const searchBtn = document.querySelector(".pesquisa button");
let weatherIcon = document.querySelector(".clima-icon");

async function checkWeather(cidade) {
    const response = await fetch(apiUrl + cidade + `&appid=${apiKey}`);

    if(response.status == 404){
        document.querySelector(".erro").style.display="block";	
        document.querySelector(".clima").style.display="none";	
    }else{
        if (response.ok) {
            const data = await response.json();
    
            document.querySelector(".cidade").innerHTML = data.name;
            document.querySelector(".tempo").innerHTML = Math.round(data.main.temp) + "Â°C";
            document.querySelector(".umidade").innerHTML = data.main.humidity + "% Umidade";
            document.querySelector(".vento").innerHTML = data.wind.speed + " km/h";
    
            if (data.weather[0].main == "Clouds") {
                weatherIcon.src = "images/nublado.png";
            } else if (data.weather[0].main == "Clear") {
                weatherIcon.src = "images/limpo.png";
            } else if (data.weather[0].main == "Rain") {
                weatherIcon.src = "images/chuva.png";
            } else if (data.weather[0].main == "Drizzle") {
                weatherIcon.src = "images/chuvisco.png";
            } else if (data.weather[0].main == "Mist") {
                weatherIcon.src = "images/nevoa.png";
            }
        }
            
        document.querySelector(".clima").style.display = "block";	
        document.querySelector(".erro").style.display="none";	
    }

    var data = await response.json();    
   
}

searchBtn.addEventListener("click", () => {
  checkWeather(searchBox.value);
});














































































































































































































































































































































































































































































